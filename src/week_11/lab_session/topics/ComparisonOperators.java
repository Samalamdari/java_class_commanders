package week_11.lab_session.topics;

public class ComparisonOperators {

    public static void main(String[] args) {

        // Comparison Operators
        /*
         * 1. >
         * 2. <
         * 3. >=
         * 4. <=
         * */

        System.out.println(10 < 20); // Just checking for one condition --> Less than
        System.out.println(10 > 20); // Just checking for one condition --> Greater than

        System.out.println(10 <= 20); // Just checking for two conditions --> Less than or equal to
        System.out.println(10 >= 20); // Just checking for two conditions --> Greater than or equal to


    }

}
