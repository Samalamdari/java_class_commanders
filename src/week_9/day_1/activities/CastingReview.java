package week_9.day_1.activities;

public class CastingReview {

    public static void main(String[] args) {

        double doubleNumber = 10;
        float floatNumber =  20F;

        System.out.println( doubleNumber );
        System.out.println( floatNumber );

        int intNumber = (int) 10.0;
        int intNumberTwo = (int) 20.5F;

        System.out.println( intNumber );
        System.out.println( intNumberTwo );

        /*
        * byte
        * short
        * int
        * long
        *
        * float
        * double
        *
        * boolean
        * char
        *
        *
        * */

    }

}
